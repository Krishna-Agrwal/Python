#To print a Statement, we use a funcion called Print()

#To print a String Data
#Syntax = print("String")
#Example :
print("Hello World")

#To print any other data except String
#Syntax = print(Integer, Float)
#Example :
print(10)
print(13.2)

#To print a Variable
#Syntax = print(Variable name)
#Example :
a = "Hello Python"
print(a)

b = 10.2
print(b)


