
#Integer Datatype : To input numeric value
a = 1
print(a)

#Float Datatype : To input decimal or fractional value
a = 1.3
print(a) 
b = 20.0
print(b)

#Boolean Datatype : Either the conditon is True or False
#Note : The first letter of True or False, should always be Captial
m = "Name"
if m == "Name":
    print(True)
else:
    print(False)

#String Datatype : To input Text Data or Any other data in text format 
#Note : While entering a String data, Quotes are mandatory either single quote or double quote.
a = "Krishna"
print(a)

#None dataype : to represent a null value
print(None)



 
