#Syntax : <datatype>(Expression)

# Possible Typescasting
#1. String to Integer : we use int() function
#Example
b = "9" #String Value
print(b+1)
#Here concatenation is not possible because a string can't be added to a integer data
c = int(b) #Integer Value
print(c+1)

#2. Integer to String : we use str() function
e = 10
print(e+"1")
#Here concatenation is not possible because a integer can't be added to a String data
f = "10"
print(f+"1")
